(_ => {
w=window;
d=document;
b=d.body;
n=b.cloneNode(true);
bs=b.style;
bcs=w.getComputedStyle(b);
bs.opacity=0.7;
h=b.parentNode;
e=d.createElement('div');
es=e.style;
es.overflow='hidden';
es.width='100%';
es.height=bcs.height;
es.position='absolute';
es.filter='blur(1px)';
es.top=0;
e.appendChild(n);
h.appendChild(e);
s=n.style;
s.width=bcs.width;
s.marginLeft=bcs.marginLeft;
s.position='absolute';
s.top=0;
s.opacity=0.7;
w.onresize=_ => {
s.width=bcs.width;
s.marginLeft=bcs.marginLeft;
};
setInterval(_ => {
t=s.top;
l=s.left;
nt=t.substring(0,t.length-2)*1+Math.ceil(Math.random()*3)-2;
nl=l.substring(0,l.length-2)*1+Math.ceil(Math.random()*3)-2;
nt=(nt>5)?4:nt;
nt=(nt<-5)?-4:nt;
nl=(nl>5)?4:nl;
nl=(nl<-5)?-4:nl;
s.top=nt+'px';
s.left=nl+'px'
},10);
})();