b=document.body;
h=b.parentNode;
n=b.cloneNode(true);
h.appendChild(n);
h.style.overflow='hidden';
s=n.style;
s.width='100%';
s.position='absolute';
s.top=0;
s.opacity=.5;
setInterval(function(){
t=s.top;
l=s.left;
nt=t.substring(0,t.length-2)*1+Math.ceil(Math.random()*3)-2;
nl=l.substring(0,l.length-2)*1+Math.ceil(Math.random()*3)-2;
nt=(nt>5)?4:nt;
nt=(nt<-5)?-4:nt;
nl=(nl>5)?4:nl;
nl=(nl<-5)?-4:nl;
s.top=nt+'px';
s.left=nl+'px';
},10);
void(0);